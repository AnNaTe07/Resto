    private void jbIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbIngresarActionPerformed
        try{
            String usuario = jtUsuario.getText();
            int contraseña = Integer.parseInt(jtContraseña.getText());
            Mesero m = md.obtenerMeseroxDNI(contraseña);
            if(!usuario.equals(m.getNombre()) && contraseña == m.getDni()){
                JOptionPane.showMessageDialog(this, "Usuario invalido");
            }
            if(usuario.equals(m.getNombre()) && contraseña == m.getDni()){
                JOptionPane.showMessageDialog(this, "Ingreso exitoso");
                dispose();
                Menu menu = new Menu();
               
                menu.setVisible(true);
                
            }
        
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Contraseña debe ser un numero");
        }catch(NullPointerException a){
            JOptionPane.showMessageDialog(this, "Contraseña invalida: ");
        }
    }//GEN-LAST:event_jbIngresarActionPerformed

    private void jtContraseñaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtContraseñaKeyTyped
        char c = evt.getKeyChar();
        if(c<'0' || c>'9'){
            evt.consume();
        }
    }//GEN-LAST:event_jtContraseñaKeyTyped

    private void jtUsuarioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtUsuarioKeyTyped
        char c = evt.getKeyChar();
        if((c<'a' || c>'z') && (c<'A' || c>'Z') && (c != ' ')){
            evt.consume();
        }
    }//GEN-LAST:event_jtUsuarioKeyTyped

